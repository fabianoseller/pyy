#Concatenar duas strings e verificar o tamanho da string resultante:
str_primeira = "Olá, "
str_segunda = "mundo!"
#Estas linhas definem as variáveis str_primeira e str_segunda com as strings "Olá, " e "mundo!", respectivamente.

str_concatenada = str_primeira + str_segunda#Esta linha concatena as strings str_primeira e str_segunda e armazena o resultado na variável str_concatenada.

print(f"String concatenada: {str_concatenada}")#Esta linha imprime a string concatenada.
print(f"Tamanho da string: {len(str_concatenada)}")#Esta linha imprime o tamanho da string concatenada, utilizando a função len()