#Comparação de duas strings:
# Operador de comparação de igualdade (==)

str1 = input("Digite a primeira string: ")
str2 = input("Digite a segunda string: ")
#Estas linhas solicitam ao usuário que digite as duas strings e armazena os valores digitados nas variáveis str1 e str2.
if str1 == str2:#Esta linha verifica se as strings str1 e str2 são iguais. Se forem iguais, o bloco de código dentro do if será executado.
  print("As strings são iguais!")#Se as strings forem iguais, esta linha imprime uma mensagem informando o usuário.
else:#Se as strings não forem iguais, o bloco de código dentro do else será executado.
  print("As strings são diferentes.")#Se as strings não forem iguais, esta linha imprime uma mensagem informando o usuário.