# # # # Classe Pessoa e Aluno:
# # # # Herança: Na programação orientada a objetos, herança é um
# # # # conceito que permite criar uma nova classe baseada em uma
# # # # classe existente. No exemplo dado, a classe Aluno herda da classe
# # # # Pessoa, o que significa que todos os atributos e métodos da classe
# # # # Pessoa são automaticamente disponibilizados para a classe Aluno. Isso permite que a classe Aluno reutilize o código da classe Pessoa e adicione novos atributos ou comportamentos específicos de aluno, como a matrícula.
# # # # Atributos:
# # # # nome, idade e sexo: São atributos da classe Pessoa que armazenam
# # # # informações básicas sobre uma pessoa, como seu nome, idade e sexo.
# # # # matricula: É um atributo específico da classe Aluno que armazena o
# # # # número de matrícula do aluno.
# # # # Classe Figura Geométrica e Círculo:
# # # # Herança: Neste caso, a classe Círculo herda da classe abstrata FiguraGeometrica. 
# # # # A herança permite que a classe Círculo herde os métodos definidos na classe base, como calcular_area() e descrever(). A classe Círculo implementa esses métodos conforme necessário para calcular a área e descrever um círculo.
# # # # Atributos:
# # # # raio: É um atributo da classe Círculo que representa o raio do círculo.
# # # # Classe Conta Bancária e Conta Poupança:
# # # # Herança: Aqui, a classe ContaPoupanca herda da classe ContaBancaria. 
# # # # Isso significa que a classe ContaPoupanca possui todos os atributos e métodos da classe ContaBancaria e pode estender ou modificar seu comportamento conforme necessário. Por exemplo, a classe ContaPoupanca adiciona o atributo rendimento_mensal e redefine o método calcular_rendimento() para incluir o rendimento mensal.
# # # # Atributos:
# # # # titular, saldo e taxa_juros: São atributos compartilhados entre as 
# # # # classes ContaBancaria e ContaPoupanca, que armazenam informações sobre 
# # # # o titular da conta, o saldo atual e a taxa de juros, respectivamente.
# # # # rendimento_mensal: É um atributo específico da classe ContaPoupanca que armazena o rendimento mensal adicional que a conta poupança gera.
