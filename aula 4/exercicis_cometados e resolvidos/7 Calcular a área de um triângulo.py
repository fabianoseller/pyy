#Calcular a área de um triângulo
base = float(input("Digite a base do triângulo: "))#Esta linha solicita ao usuário que digite a base do triângulo e armazena o valor digitado na variável base.A função float() converte o valor digitado para um número de ponto flutuante.
altura = float(input("Digite a altura do triângulo: "))#Esta linha solicita ao usuário que digite a altura do triângulo e armazena o valor digitado na variável altura.A função float() converte o valor digitado para um número de ponto flutuante.

area = (base * altura) / 2#Esta linha calcula a área do triângulo usando a fórmula:

print(f"A área do triângulo é: {area}")#Esta linha imprime a área do triângulo.