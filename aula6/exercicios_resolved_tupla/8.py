tupla_pares = tuple(range(2, 11, 2))
#Esta linha cria uma tupla chamada tupla_pares usando a função tuple().
#A função tuple() recebe um objeto iterável como argumento e retorna uma tupla contendo os elementos do objeto iterável.
#Neste caso, o objeto iterável é a função range(2, 11, 2).
#A função range() gera uma sequência de números começando em 2 e terminando em 10 (sem incluir o 11), com um passo de 2.
#Ou seja, a sequência gerada pela função range() é: [2, 4, 6, 8, 10].
#A função tuple() converte essa sequência em uma tupla, que é atribuída à variável tupla_pares.')
print(f"Tupla com valores pares de 1 a 10: {tupla_pares}")
